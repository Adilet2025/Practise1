# Boolean Comparison examples in Python

a = 10
b = 7

# Greater than
print(a > b)      # True, because 10 is greater than 7

# Less than
print(a < b)      # False, because 10 is not less than 7

# Equal to
print(a == b)     # False, because 10 is not equal to 7

# Not equal to
print(a != b)     # True, because 10 is different from 7

# Greater than or equal to
print(a >= b)     # True, because 10 is greater than 7

# Less than or equal to
print(a <= b)     # False, because 10 is not less than or equal to 7
